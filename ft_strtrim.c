#include "libft.h"

char	*ft_strtrim(const char *s1, const char *set)
{
	size_t	i;
	size_t	j;
	char	*res;

	if (!s1 || !set)
		return (NULL);
	i = 0;
	while (s1[i] && ft_strchr(set, s1[i]))
		i++;
	j = ft_strlen(s1);
	while (j > i && ft_strchr(set, s1[j - 1]))
		j--;
	res = (char *) malloc(sizeof(char) * (j - i + 1));
	if (!res)
		return (NULL);
	ft_memcpy(res, &s1[i], j - i);
	res[j - i] = '\0';
	return (res);
}
